let changeMe = true; //Declare variable changeMe and initialize it to true
changeMe = false; //Reassign variable to false
console.log(changeMe); //Output: false

/* The let keyword signals that the variable can be changed. Here we declare the changeMe variable to have a value of true and later change it to false.
  We can also declare a variable with no value. When we do this the variable will be initialized with undefined as its value. */
