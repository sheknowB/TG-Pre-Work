let sale = true;

sale = false;

if(sale) {
  console.log('Time to buy!');
} else {
  console.log('Time to wait for a sale.')
}
//If sale is true, prints "Time to buy!". If sale is false, prints "Time to wait for a sale."

/*If we want a code to run when our condition is false we include an else statement. An else statement must be paired with an if statement. Together they are referred to as an if...else statement.
  If...else statements allow us to automate solutions to a yes-or-no questions known as binary decisions. */
