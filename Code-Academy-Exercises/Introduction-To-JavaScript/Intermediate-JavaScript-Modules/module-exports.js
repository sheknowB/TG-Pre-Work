let Airplane = {}; // Create an object named Airplane
Airplane.myAirplane = "StarJet"; // Add property myAirplane

module.exports = Airplane; // Export the module Airplane

/* When working with modules, we define them and then export them using the statement module.exports. module is a variable that represents the module, and exports exposes the module as an object.

  The pattern of exporting a module:
    1.  Define an object to represent the module
    2.  Add data or behavior to the module
    3.  Export the module
*/
